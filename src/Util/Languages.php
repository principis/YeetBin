    /**
     * Returns the languages after sorting and removing 'plaintext'.
     *
     * @return string[]
     */
    public static function getForView() :array
    {
        $result = self::$languages;

        natcasesort($result);
        unset($result['plaintext']);

        return $result;
    }

    public static function getLanguage(string $key) :?string
    {
        return self::$languages[$key] ?? null;
    }
